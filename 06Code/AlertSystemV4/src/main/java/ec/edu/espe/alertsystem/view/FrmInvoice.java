    private void loadInvoicesTable() {

        DefaultTableModel model = (DefaultTableModel) tblInvoice.getModel();
        model.setRowCount(0);

        MongoCollection<Document> collection
                = MongoConnection.getDatabase().getCollection("invoices");

        SimpleDateFormat outputFormat = new SimpleDateFormat("dd/MM/yy");
        SimpleDateFormat inputFormat
                = new SimpleDateFormat("MMM dd, yyyy, hh:mm:ss a", Locale.ENGLISH);

        for (Document doc : collection.find()) {

            int invoiceNumber = doc.getInteger("invoiceNumber", 0);
            String paymentDateStr = doc.getString("paymentDate");
            double amountPaid = doc.getDouble("amountPaid");
            String details = doc.getString("details");
            String status = doc.getString("status");

            String formattedDate = "";

            // ðŸ‘‡ SOLO si NO estÃ¡ pendiente
            if (!"Pendiente".equalsIgnoreCase(status) && paymentDateStr != null) {
                try {
                    Date date = inputFormat.parse(paymentDateStr);
                    formattedDate = outputFormat.format(date);
                } catch (Exception e) {
                    formattedDate = "";
                }
            }

            model.addRow(new Object[]{
                invoiceNumber,
                formattedDate,
                amountPaid,
                details,
                status
            });
        }
    }

