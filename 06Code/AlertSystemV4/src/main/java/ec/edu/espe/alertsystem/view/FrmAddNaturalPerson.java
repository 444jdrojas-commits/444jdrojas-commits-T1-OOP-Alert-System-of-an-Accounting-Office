    private void btnSaveBussinesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveBussinesActionPerformed

        String nombre = txtName.getText().trim();
        if (nombre.isEmpty() || !Validation.isAlphabetic(nombre)) {
            JOptionPane.showMessageDialog(null, "El nombre solo debe contener letras y no puede estar vacío.");
            txtName.requestFocus();
            return;
        }

        String nationality = txtNationality.getText().trim();
        if (nationality.isEmpty() || !Validation.isAlphabetic(nationality)) {
            JOptionPane.showMessageDialog(null, "El representante solo debe contener letras y no puede estar vacío.");
            txtNationality.requestFocus();
            return;
        }
        String ruc = txtPhone.getText().trim();
        if (ruc.isEmpty() || !Validation.isInteger(ruc)) {
            JOptionPane.showMessageDialog(null, "El RUC/CI solo debe contener números y no puede estar vacío.");
            txtPhone.requestFocus();
            return;
        }

        String telefono = txtIdentification.getText().trim();
        if (telefono.isEmpty() || !Validation.isInteger(telefono)) {
            JOptionPane.showMessageDialog(null, "El teléfono solo debe contener números y no puede estar vacío.");
            txtIdentification.requestFocus();
            return;
        }
        String email = txtEmail.getText().trim();
        if (email.isEmpty() || !Validation.isEmail(email)) {
            JOptionPane.showMessageDialog(null, "Ingrese un correo electrónico válido.");
            txtEmail.requestFocus();
            return;
        }

        String ciudad = txtCity.getText().trim();
        if (ciudad.isEmpty() || !Validation.isAlphabetic(ciudad)) {
            JOptionPane.showMessageDialog(null, "La ciudad solo debe contener letras y no puede estar vacía.");
            txtCity.requestFocus();
            return;
        }

        String ocuppation = txtOcuppation.getText().trim();
        if (ocuppation.isEmpty() || !Validation.isAlphabetic(ocuppation)) {
            JOptionPane.showMessageDialog(null, "La ciudad solo debe contener letras y no puede estar vacía.");
            txtCity.requestFocus();
            return;
        }

        String calle = txtStreet.getText().trim();
        if (calle.isEmpty()) {
            JOptionPane.showMessageDialog(null, "La calle no puede estar vacía.");
            txtStreet.requestFocus();
            return;
        }

        String sector = txtSector.getText().trim();
        if (sector.isEmpty()) {
            JOptionPane.showMessageDialog(null, "El sector no puede estar vacío.");
            txtSector.requestFocus();
            return;
        }

        readValues(); 

        int option = JOptionPane.showConfirmDialog(
                rootPane,
                "¿Desea guardar esta Persona Natural?",
                "Guardar",
                JOptionPane.YES_NO_CANCEL_OPTION
        );

        if (option == JOptionPane.YES_OPTION) {

            NaturalPersonDAO dao = new NaturalPersonDAO();
            boolean saved = dao.save(naturalPerson);

            if (saved) {
                JOptionPane.showMessageDialog(rootPane,
                        "Guardado con éxito\n" + naturalPerson.toString());
            } else {
                JOptionPane.showMessageDialog(rootPane,
                        "Error al guardar",
                        "Error",
                        JOptionPane.ERROR_MESSAGE);
            }

            emptyFields();

        } else if (option == JOptionPane.NO_OPTION) {

            JOptionPane.showMessageDialog(rootPane,
                    "Los datos no serán guardados",
                    "Advertencia",
                    JOptionPane.WARNING_MESSAGE);

            emptyFields();

        } else {
            txtName.requestFocus();
        }
    
    }//GEN-LAST:event_btnSaveBussinesActionPerformed

    private void readValues() {
    String name;
    String identification;
    String nationality;
    Date birthDate;
    String phone;
    String email;
    String occupation;
    String gender;

    String city;
    String street;
    String sector;

    name = txtName.getText();
    identification = txtIdentification.getText();
    nationality = txtNationality.getText();

    LocalDate localDate = dtBirthDate.getDate();
    birthDate = java.sql.Date.valueOf(localDate);

    phone = txtPhone.getText();
    email = txtEmail.getText();
    occupation = txtOcuppation.getText();

    if (radGenderMale.isSelected()) {
        gender = "Male";
    } else {
        gender = "Female";
    }

    city = txtCity.getText();
    street = txtStreet.getText();
    sector = txtSector.getText();

    Address address = new Address(city, street, sector);

    naturalPerson = new NaturalPerson(name, identification, nationality, birthDate, phone, email, occupation, gender, address);
}

    private void emptyFields() {
    txtName.setText("");
    txtIdentification.setText("");
    txtNationality.setText("");
    dtBirthDate.setText("");
    txtPhone.setText("");
    txtEmail.setText("");
    txtOcuppation.setText("");
    radGenderFemale.setSelected(true);

    txtCity.setText("");
    txtStreet.setText("");
    txtSector.setText("");
}

