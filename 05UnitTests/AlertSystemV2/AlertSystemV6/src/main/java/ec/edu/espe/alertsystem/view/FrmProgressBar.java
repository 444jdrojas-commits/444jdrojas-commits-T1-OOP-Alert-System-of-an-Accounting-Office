    private void startAnimation() {
        Thread hilo = new Thread(new Runnable() {
            @Override
            public void run() {
                try {
                    for (int i = 0; i <= 100; i++) {
                        Thread.sleep(40); 
                        jProgressBar1.setValue(i);
                        if(i == 20) jLabel1.setText("Cargando mÃ³dulos...");
                        if(i == 50) jLabel1.setText("Conectando base de datos...");
                        if(i == 80) jLabel1.setText("Iniciando sistema...");
                    }
                    abrirSiguienteVentana();
                } catch (InterruptedException e) {
                    e.printStackTrace();
                }
            }
        });
        hilo.start();
    }
    
    private void abrirSiguienteVentana() {
        this.dispose(); 
        FrmAlertSystemLogin frmAlertSystemLogin = new FrmAlertSystemLogin();
        frmAlertSystemLogin.setVisible(true);
        this.dispose();
    }
