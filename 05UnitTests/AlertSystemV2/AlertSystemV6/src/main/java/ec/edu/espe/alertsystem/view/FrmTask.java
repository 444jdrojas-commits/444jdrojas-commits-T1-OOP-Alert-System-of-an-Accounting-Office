    private void btnCreateTaskActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateTaskActionPerformed

        String description = txtDescription.getText().trim();
        if (description.isEmpty() || !Validation.isAlphabetic(description)) {
            JOptionPane.showMessageDialog(null, "La descripcion solo debe contener letras y no puede estar vacío.");
            txtDescription.requestFocus();
            return;
        }

        LocalDate deliveryDate = dtDeliveryDate.getDate();
        if (deliveryDate == null) {
            JOptionPane.showMessageDialog(null, "Debe seleccionar una fecha.");
            return;
        }

        if (!Validation.isDateAfterToday(deliveryDate)) {
            JOptionPane.showMessageDialog(null, "La fecha debe ser posterior a la fecha actual.");
            return;
        }

        Document document = null;

        if (radDocumentYes.isSelected()) {

            LocalDate reviewDay = dtReviewDay.getDate();
            if (reviewDay == null || !Validation.isFutureDate(reviewDay)) {
                JOptionPane.showMessageDialog(null,
                        "La fecha del documento debe ser futura (no hoy ni pasada).");
                dtReviewDay.requestFocus();
                return;
            }

            String name = txtName.getText().trim();
            if (name.isEmpty()) {
                JOptionPane.showMessageDialog(null,
                        "El número de documento no puede estar vacío.");
                txtName.requestFocus();
                return;
            }

            String typeDocument = txtTypeDocument.getText().trim();
            if (typeDocument.isEmpty()) {
                JOptionPane.showMessageDialog(null,
                        "El tipo de documento no puede estar vacío.");
                txtDescription.requestFocus();
                return;
            }

            String detail = txtDetail.getText().trim();
            if (detail.isEmpty()) {
                JOptionPane.showMessageDialog(null,
                        "El detalles del documento no puede estar vacío.");
                txtDetail.requestFocus();
                return;
            }
        }
        Task task = new Task(
                0, 
                description,
                cmbCustomer.getSelectedItem().toString(),
                java.sql.Date.valueOf(deliveryDate)
        );

        task.setAssignedTo(cmbAssignedTo.getSelectedItem().toString());
        task.setDocument(document);

        int option = JOptionPane.showConfirmDialog(
                this,
                "¿Desea guardar esta tarea?",
                "Confirmar",
                JOptionPane.YES_NO_OPTION
        );

        if (option == JOptionPane.YES_OPTION) {

            TaskController controller = new TaskController();
            boolean saved = controller.save(task);

            if (saved) {
                JOptionPane.showMessageDialog(this,
                        "Tarea guardada correctamente.");
                clearFields();
            } else {
                JOptionPane.showMessageDialog(this,
                        "Error al guardar la tarea.",
                        "Error",
                        JOptionPane.ERROR_MESSAGE);
            }
        }

    }//GEN-LAST:event_btnCreateTaskActionPerformed

    private void clearFields() {

        txtDescription.setText("");
        dtDeliveryDate.setDate(null);
        cmbCustomer.setSelectedIndex(0);
        cmbAssignedTo.setSelectedIndex(0);

        radDocumentNo.setSelected(true);

        txtName.setText("");
        txtTypeDocument.setText("");
        txtDetail.setText("");
        dtReviewDay.setDate(null);

        txtName.setEnabled(false);
        txtTypeDocument.setEnabled(false);
        txtDetail.setEnabled(false);
        dtReviewDay.setEnabled(false);
    }

